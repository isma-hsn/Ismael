int	ft_str_is_lowercase(char *str)
{
	int	j;
	int	i;

	i = 0;
	j = 1;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'a' && str[i] <= 'z'))
			j = 0;
	i++;
	}
	return (j);
}
