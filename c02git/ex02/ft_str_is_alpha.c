int	ft_str_is_alpha(char *str)
{
	int	i;
	int	j;

	j = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'a' && str[i] <= 'z'))
		{
			if (!(str[i] >= 'A' && str[i] <= 'Z'))
				j = 0;
		}
	i++;
	}
	return (j);
}
